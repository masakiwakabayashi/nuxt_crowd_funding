  select
    ds.*,
    coalesce(r.estimated_delivery, now() + interval '30 days') as due_date,
    case
      when ds.status = '完了' then coalesce(r.estimated_delivery, now())
      else null
    end as completed_at,
    r.project_id
  from delivery_seed ds
  join rewards r
    on r.id = ds.reward_id
)
